	public Circle() {
		super();
	}

	@Override
	public int getNum1() {
		
		return super.getNum1();
	}
	
	@Override
	public void setNum1(int num1) {
		
		super.setNum1(num1);
	}
	
	public double circle() {
		return Math.PI * getNum1();
	}










	

	
